def translate_msgid(msgid):
    """Multi-pass translation."""
    if any('\u4e00' <= c <= '\u9fff' for c in msgid) and not any(c.isascii() and c.isalpha() for c in msgid):
        return msgid
    
    # Pass 1: exact match
    if msgid in GLOSSARY:
        return GLOSSARY[msgid]
    
    # Pass 2: word-by-word translation using glossary (longest first)
    result = msgid
    sorted_glossary = sorted(GLOSSARY.items(), key=lambda x: -len(x[0]))
    changed = False
    for en, zh in sorted_glossary:
        if en in result and len(en) > 1:
            result = result.replace(en, zh)
            changed = True
    
    if changed:
        return result
    
    # Pass 3: return as-is (truly unique)
    return msgid

# Process
with open(PO_PATH, 'r', encoding='utf-8') as f:
    content = f.read()

pattern = re.compile(
    r'(msgid "([^"]*)")\nmsgstr "\[待审\] [^"]*"',
    re.MULTILINE
)

replacements = 0
def replace_match(m):
    global replacements
    msgid = m.group(2)
    new_translation = translate_msgid(msgid)
    replacements += 1
    if new_translation == msgid:
        return f'{m.group(1)}\nmsgstr "[待审] {msgid}"'  # Keep as pending
    return f'{m.group(1)}\nmsgstr "{new_translation}"'

content = pattern.sub(replace_match, content)

print(f"处理: {replacements} 条")

# Count remaining
remaining = content.count('msgstr "[待审]')
translated = replacements - remaining
print(f"新增翻译: {translated}")
print(f"仍待审: {remaining}")

with open(PO_PATH, 'w', encoding='utf-8', newline='') as f:
    f.write(content)

